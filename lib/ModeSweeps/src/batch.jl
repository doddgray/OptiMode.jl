# Batch deployment: directory layout, manifest, SLURM array-job generation/submission,
# and a `:local` process backend (used for testing and small batches).
#
# Batch directory layout:
#   <dir>/batch.toml        manifest (everything needed to re-load the batch later)
#   <dir>/params.json       the parameter sets (one JSON object per task)
#   <dir>/setup.jl          copy of the user's problem-setup script
#   <dir>/runtask.jl        generated worker entry script
#   <dir>/job.sbatch        generated SLURM array-job submission script (slurm backend)
#   <dir>/tasks/            per-task results: task_NNNNNN.json (+ .h5), .done/.failed markers
#   <dir>/logs/             SLURM / worker logs

"""
    SlurmConfig(; kwargs...)

SLURM submission options for [`deploy_batch`](@ref).

- `time`, `partition`, `mem`, `cpus_per_task`, `gres`: standard `#SBATCH` options
  (empty/`nothing` values are omitted).
- `max_concurrent`: throttle for the array job (`--array=1-N%max_concurrent`).
- `julia`: the `julia` executable to use on the cluster (default `"julia"`).
- `project`: Julia project to activate on the workers. Defaults to the deploying
  session's active project — appropriate when the cluster shares the filesystem.
  Set it to the path of the project on the cluster otherwise.
- `julia_flags`: extra flags for the worker `julia` invocation (e.g. `["-t","4"]`).
- `sbatch_args`: extra raw arguments appended to the `sbatch` call.
- `ssh`: if set (e.g. `"user@login.cluster"`), the batch directory is rsync'ed to
  `remote_dir` on that host, `sbatch` runs over ssh, and `gather_batch`/`batch_status`
  fetch results back over ssh. When `nothing` (default), the batch directory itself is
  assumed to live on a filesystem shared with the compute nodes.
- `remote_dir`: remote path of the batch directory (ssh mode only).
"""
Base.@kwdef struct SlurmConfig
    time::String = "1:00:00"
    partition::String = ""
    mem::String = ""
    cpus_per_task::Int = 1
    gres::String = ""
    max_concurrent::Int = 0
    julia::String = "julia"
    project::String = abspath(Base.active_project())
    julia_flags::Vector{String} = String[]
    sbatch_args::Vector{String} = String[]
    ssh::Union{Nothing,String} = nothing
    remote_dir::Union{Nothing,String} = nothing
end

"""
In-memory handle for a deployed (or re-loaded) batch of mode simulations. All state
lives in the batch directory, so a `BatchInfo` can be re-created in a new Julia session
with [`load_batch`](@ref).
"""
struct BatchInfo
    dir::String
    manifest::Dict{String,Any}
    params::Vector{NamedTuple}
end

Base.length(b::BatchInfo) = length(b.params)
n_tasks(b::BatchInfo) = length(b.params)
_taskdir(b::BatchInfo) = joinpath(b.dir, "tasks")
_task_id_str(i::Int) = @sprintf("%06i", i)
_task_base(b::BatchInfo, i::Int) = joinpath(_taskdir(b), "task_" * _task_id_str(i))

function Base.show(io::IO, b::BatchInfo)
    print(io, "BatchInfo(\"", get(b.manifest, "name", "?"), "\", ", length(b.params),
        " tasks, backend=", get(b.manifest, "backend", "?"), ", dir=", b.dir, ")")
end

# ---------------------------------------------------------------------------------
# script generation
# ---------------------------------------------------------------------------------

function _runtask_script()
    return """
    # Worker entry point (generated by ModeSweeps): runs one task of a batch.
    #   julia --project=<project> runtask.jl <batchdir> <task index>
    using ModeSweeps
    ModeSweeps.run_task(ARGS[1], parse(Int, ARGS[2]))
    """
end

function _sbatch_script(name::String, n::Int, cfg::SlurmConfig, batchdir_on_cluster::String)
    lines = String[
        "#!/bin/bash",
        "#SBATCH --job-name=$(name)",
        "#SBATCH --output=$(joinpath(batchdir_on_cluster, "logs", "slurm-%A_%a.out"))",
        "#SBATCH --array=1-$(n)" * (cfg.max_concurrent > 0 ? "%$(cfg.max_concurrent)" : ""),
        "#SBATCH --time=$(cfg.time)",
        "#SBATCH --cpus-per-task=$(cfg.cpus_per_task)",
    ]
    isempty(cfg.partition) || push!(lines, "#SBATCH --partition=$(cfg.partition)")
    isempty(cfg.mem) || push!(lines, "#SBATCH --mem=$(cfg.mem)")
    isempty(cfg.gres) || push!(lines, "#SBATCH --gres=$(cfg.gres)")
    flags = join(cfg.julia_flags, " ")
    push!(lines, "")
    push!(lines, "$(cfg.julia) --project=$(cfg.project) $(flags) " *
                 "$(joinpath(batchdir_on_cluster, "runtask.jl")) $(batchdir_on_cluster) \$SLURM_ARRAY_TASK_ID")
    return join(lines, "\n") * "\n"
end

